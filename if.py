#โปรแกรมคำนวณราคาตั๋วหนัง
 
#step 1: สร้าง input รับอายุและอย่าลืมแปลงค่าจาก str เป็น int
age = int(input("คุณอายุเท่าไหร่: "))
 
#step 2: สร้าง if-else ที่เช็กว่าอายุน้อยกว่าหรือเท่ากับ 12 ไหม
if age <= 12:
    print("คุณได้ราคาตั๋วเด็ก ราคาตั๋ว 120 บาท")
else:
    has_stdcard = input("คุณมีบัตรนักศึกษาหรือไม่? (Y/N)").upper()
    if has_stdcard == "Y":
        print("ราคาตั๋ว 160 บาท")
    else:
        print("ราคาตั๋ว 200 บาท")